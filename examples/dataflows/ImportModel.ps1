<#
.Synopsis
    Imports a dataflow model.json into a Power BI workspace.
.Description
    Imports a dataflow model.json into a Power BI workspace. The script can either create a new model or overwrite a model with the same name
    This script will fail if the target workspace does not exist.
    This script uses the Power BI Management module for Windows PowerShell. If this module isn't installed, install it by using the command 'Install-Module -Name MicrosoftPowerBIMgmt -Scope CurrentUser'.
.Parameter Workspace
    [Required] The name of the workspace you'd like to import a dataflow model.json
.Parameter File
    [Required] Path to the model.json file
.Parameter Overwrite
    [Optional]: A flag to indicate whether to overwrite a model with the same name if it exists. Default is false
.Parameter Environment
    [Optional]: A flag to indicate specific Power BI environments to log in to (Public, Germany, USGov, China, USGovHigh, USGovMil). Defailt is Public
.Parameter V
    [Optional]: A flag to indicate whether to produce verbose output. Default is false
.Example
    PS C:\> .\ImportModel.ps1 -Workspace "Workspace1" -File C:\MyModel.json -Overwrite
	Imports the dataflow MyModel.json into the Power BI workspace "Workspace1"
#>
Using module ".\Graph.psm1"
param (
    [Parameter(Mandatory=$true)]
    [string] $Workspace,
    [Parameter(Mandatory=$true)]
    [string] $File,
    [Parameter(Mandatory=$false)]
    [switch]$Overwrite = $false,
	[Parameter(Mandatory=$false)]
    [string] $Environment,
    [Parameter(Mandatory=$false)]
    [switch]$v = $false
)
Begin
{
#region Initialization
    $ErrorActionPreference="SilentlyContinue"
    Stop-Transcript | out-null
    $ErrorActionPreference = "Continue"
    
    Import-Module (Join-Path $PSScriptRoot DFUtils.psm1) -Force
    Import-Module (Join-Path $PSScriptRoot Graph.psm1) -Force
    DFLogMessage("SetVerbose : $v")
    SetVerbose($v)
#endregion
}
Process
{
    # Login to PowerBi and fetch the workspace id
    DFLogMessage("Overwrite : $Overwrite")
    LoginPowerBi($Environment)
	$workspaceId = GetWorkspaceIdFromName($Workspace)
    $dataflows = GetDataflowsForWorkspace($workspaceId)
    
    # Read the file
    $json = ReadModelJson($File)
    $dataflowName = $json.name
    DFLogMessage("Dataflow Name: $dataflowName")
    
    # Import the dataflow
    $overwriteModelId = GetOverrwiteModelId $dataflows  $Overwrite  $dataflowName
    $importedDataflow = ImportModel $workspaceId  $overwriteModelId $json $dataflows
    DFLogMessage("Imported dataflow id: [" + $importedDataflow.objectId + "] Name=[" + $importedDataflow.name + "]")
}
End
{
    Write-Host "ImportModel completed"
}
